'''lista = [2,3,4,5,6] a lista é definida pelos [] e pode armezenar diversas variáveis em um só local

lista = ['Mardel',2, 2.4, true]

A lista pode ser chamada de ARRAY

alem disso ela pode armezanar diversos tipos de variáveis
'''
lista_num = [1,2,3,4,5]

lista_vog = ['a','e','i','o','u']

print(lista_num)

'''
É possível acessar apenas 1 posição da lista utilizando o [] para definir a posição, sendo a primeira posição **0**
'''

print(lista_vog[1])

'''
Também é possivel acessar os elementos de uma lista utilizando numeros negativos, dessa forma ele inicia a contagem de trás pra frente
sendo -1 a ultima posição, -2 a penultima e assim sucessivamente
'''

print(lista_num[-1])
